package com.wordpdf.core;

public class EnZh {

	private String en;
	private String zh;
	public EnZh(String en, String zh) {
		super();
		this.en = en;
		this.zh = zh;
	}
	public String getEn() {
		return en;
	}
	public void setEn(String en) {
		this.en = en;
	}
	public String getZh() {
		return zh;
	}
	public void setZh(String zh) {
		this.zh = zh;
	}
}
